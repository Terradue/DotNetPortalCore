namespace Terradue.Portal {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    /// <summary>Represents a product type featured on the portal.</summary>
    public class ProductType : Entity {

        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new ProductType instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        */
        public ProductType(IfyContext context) : base(context) {}
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new ProductType instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        /// <returns>the created ProductType object</returns>
        */
        public static ProductType GetInstance(IfyContext context) {
            return new ProductType(context);
        }
        
    }

}

