namespace Terradue.Portal {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    /// <summary>Represents a scheduler class that can be assigned to a scheduler.</summary>
    public class SchedulerClass : Entity {
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new SchedulerClass instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        */
        public SchedulerClass(IfyContext context) : base(context) {}
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new SchedulerClass instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        /// <returns>the created SchedulerClass object</returns>
        */
        public static new SchedulerClass GetInstance(IfyContext context) {
            return new SchedulerClass(context);
        }
        
    }
}

