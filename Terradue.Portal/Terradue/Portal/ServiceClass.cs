namespace Terradue.Portal {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    /// <summary>Represents a service class that can be assigned to a service.</summary>
    public class ServiceClass : Entity {
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new ServiceClass instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        */
        public ServiceClass(IfyContext context) : base(context) {}
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new ServiceClass instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        /// <returns>the created ServiceClass object</returns>
        */
        public static new ServiceClass GetInstance(IfyContext context) {
            return new ServiceClass(context);
        }
        
    }

}

