namespace Terradue.Portal {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    /// <summary>Represents a user group</summary>
    public class ManagerRole : Entity {

        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new ManagerRole instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        */
        public ManagerRole(IfyContext context) : base(context) {}
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new ManagerRole instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        /// <returns>the created ManagerRole object</returns>
        */
        public static new ManagerRole GetInstance(IfyContext context) {
            return new ManagerRole(context);
        }
        
    }

}

