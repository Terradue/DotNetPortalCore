namespace Terradue.Portal {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    /// <summary>Represents a Frequently Asked Question (FAQ)</summary>
    public class Faq : Entity {

        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new Faq instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        */
        public Faq(IfyContext context) : base(context) {}
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new Faq instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        /// <returns>the created Faq object</returns>
        */
        public static Faq GetInstance(IfyContext context) {
            return new Faq(context);
        }
        
    }

}

