namespace Terradue.Portal {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    /// <summary>Represents a project featured on the portal.</summary>
    public class Project : Entity {

        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new Project instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        */
        public Project(IfyContext context) : base(context) {}
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new Project instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        /// <returns>the created Project object</returns>
        */
        public static new Project GetInstance(IfyContext context) {
            return new Project(context);
        }
        
    }

}

