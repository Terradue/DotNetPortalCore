namespace Terradue.Portal {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    /// <summary>Represents a Light Grid Engine (LGE) host.</summary>
    public class LightGridEngine : Entity {

        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new LightGridEngine instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        */
        public LightGridEngine(IfyContext context) : base(context) {}
        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new LightGridEngine instance.</summary>
        /*!
        /// <param name="context">The execution environment context.</param>
        /// <returns>the created LightGridEngine object</returns>
        */
        public static new LightGridEngine GetInstance(IfyContext context) {
            return new LightGridEngine(context);
        }
        
    }

}

