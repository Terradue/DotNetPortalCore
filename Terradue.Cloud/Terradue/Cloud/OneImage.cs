using System.Runtime.Serialization;
using Terradue.OpenNebula;





namespace Terradue.Cloud {

    

    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------

    

    //! Represents a Globus computing resource that is accessed through an LGE interface.
	[Serializable]
	[DataContract]
    public class OneImage : VirtualDisk {
        
        //---------------------------------------------------------------------------------------------------------------------
        
        public OneImage(IfyContext context) : base(context) {}

        //---------------------------------------------------------------------------------------------------------------------

        public OneImage(IfyContext context, IMAGE image, CloudProvider provider) : base(context){
            this.RemoteId = image.ID;
            this.Name = image.NAME;
            this.Provider = Provider;
        }

        
        //---------------------------------------------------------------------------------------------------------------------

        /// <summary>Creates a new XmlRpcImage instance.</summary>
        /// <param name="context">the execution environment context</param>
        /// <returns>the created XmlRpcImage object</returns>
        public static new OneImage GetInstance(IfyContext context) {
            return new OneImage(context);
        }
        
        //---------------------------------------------------------------------------------------------------------------------


        
    }


}

